ruta1="/content/solicitud_creditos_info.csv"

ruta2="/content/Employees.csv"

import pandas as pd

import numpy as np

import matplotlib.pyplot as plt

import seaborn as sns

import warnings

warnings.filterwarnings('ignore')

dfSolicitudes = pd.read_csv(ruta1) # Carga los datos a la memoria
print(dfSolicitudes.info()) # Comentario: muestra la información general de la tabla de datos
dfSolicitudes.head() # Muestra las primeras filas de la tabla de datos

dfSolicitudes.describe(include='all')

# Frecuencias de la variable default
dfSolicitudes['default'].value_counts()

dfempleados = pd.read_csv(ruta2) # Carga los datos a la memoria
print(dfempleados.info()) # Comentario: muestra la información general de la tabla de datos
dfempleados.head()

# ==============================
# UNIR LOS DOS DATASET
# ==============================

dfunido = dfSolicitudes.merge(
    dfempleados,
    on='Employee',
    how='left'
)

print(f"Dimensiones del dataset unido: {dfunido.shape[0]:,} filas x {dfunido.shape[1]} columnas")

print(f"Valores faltantes en Employee: {dfSolicitudes['Employee'].isna().sum()}")

# Variables relacionadas con empleados
for col in [
    'Employee',
    'employee_age',
    'employe_gender',
    'exp_previa',
    'employee_estudios',
    'resul_prub_seleccion',
    'eval_jefeRH'
]:
    print(f" * {col}")

# Validar que el merge quedó correcto
display(
    dfunido[
        [
            'Employee',
            'employee_age',
            'employe_gender',
            'employee_estudios'
        ]
    ].head()
)

"""MORA GENERAL"""

mora_general = dfSolicitudes['default'].mean() * 100

print(f"Mora general: {mora_general:.2f}%")

"""**GRÁFICO MORA GENERAL**"""

mora = dfSolicitudes['default'].value_counts()

plt.figure(figsize=(6,4))

mora.plot(kind='bar')

plt.title('Clientes en Mora vs No Mora')
plt.xlabel('Default')
plt.ylabel('Cantidad')

plt.show()

"""**MORA POR OFICINA**"""

mora_oficina = dfSolicitudes.groupby('Office')['default'].mean() * 100

mora_oficina = mora_oficina.sort_values(ascending=False)

display(mora_oficina.head(10))

top_oficinas = mora_oficina.head(10)

plt.figure(figsize=(10,5))

top_oficinas.plot(kind='bar')

plt.title('Top 10 Oficinas con Mayor Mora')
plt.xlabel('Oficina')
plt.ylabel('% Mora')

plt.show()

"""**RELACIÓN CRÉDITO / INGRESO**"""

dfSolicitudes['ratio_credito_ingreso'] = (
    dfSolicitudes['loan_amount'] / dfSolicitudes['total_income']
)

dfSolicitudes['ratio_credito_ingreso'].describe()

plt.figure(figsize=(8,5))

plt.hist(
    dfSolicitudes['ratio_credito_ingreso'].dropna(),
    bins=30
)

plt.title('Relación Crédito / Ingreso')
plt.xlabel('Ratio Crédito / Ingreso')
plt.ylabel('Frecuencia')

plt.grid(axis='y', linestyle='--', alpha=0.5)

plt.show()

"""**MORA POR TIPO DE INGRESO**"""

mora_ingreso = dfSolicitudes.groupby('income_type')['default'].mean() * 100

mora_ingreso = mora_ingreso.sort_values(ascending=False)

display(mora_ingreso)

"""**EDAD DE CLIENTE MOROSOS**"""

# Clientes en mora
clientes_mora = dfSolicitudes[dfSolicitudes['default'] == 1]

# Gráfico corregido
plt.figure(figsize=(8,5))

plt.hist(
    clientes_mora['age_years'].dropna(),
    bins=20
)

plt.title('Edad de Clientes en Mora')
plt.xlabel('Edad (años)')
plt.ylabel('Cantidad')

plt.show()

"""**MATRIZ DE CORRELACION**"""

numericas = dfSolicitudes.select_dtypes(include='number')

correlacion = numericas.corr()

plt.figure(figsize=(12,8))

plt.imshow(correlacion, aspect='auto')

plt.colorbar()

plt.xticks(range(len(correlacion.columns)), correlacion.columns, rotation=90)
plt.yticks(range(len(correlacion.columns)), correlacion.columns)

plt.title('Matriz de Correlación')

plt.show()

# Convertir edad de días a años
dfSolicitudes['age_years'] = abs(dfSolicitudes['age']) / 365

# Gráfico corregido
plt.figure(figsize=(8,5))

plt.hist(
    dfSolicitudes['age_years'].dropna(),
    bins=25
)

plt.title('Distribución de Edad de los Clientes')
plt.xlabel('Edad (años)')
plt.ylabel('Cantidad')

plt.show()

plt.figure(figsize=(8,5))

plt.hist(dfSolicitudes['loan_amount'].dropna(), bins=30)

plt.title('Distribución del Valor de los Créditos')
plt.xlabel('Monto del crédito')
plt.ylabel('Cantidad')

plt.show()

# Limitar valores extremos usando percentil 99
limite = dfSolicitudes['total_income'].quantile(0.99)

ingresos_filtrados = dfSolicitudes[
    dfSolicitudes['total_income'] <= limite
]

plt.figure(figsize=(8,5))

plt.hist(
    ingresos_filtrados['total_income'].dropna(),
    bins=30
)

plt.title('Distribución de Ingresos de los Clientes')
