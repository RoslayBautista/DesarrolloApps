plt.ylabel('Cantidad')

plt.grid(axis='y', linestyle='--', alpha=0.5)

plt.show()

plt.figure(figsize=(8,6))

plt.scatter(
    dfSolicitudes['total_income'],
    dfSolicitudes['loan_amount'],
    alpha=0.3
)

plt.title('Ingresos vs Valor del Crédito')
